// ------------------------------------------------------------------ nt hardening
// posixmodule uses multi-phase init: PyInit_nt returns a PyModuleDef whose exec slots
// populate the module. We hand out a copy of that def with one extra exec slot appended
// that deletes the process-creation primitives. The interpreter copies the inittab at
// startup, so this wrapper - not a post-init fixup - is what makes the scrub stick
// across re-imports.
static const char* kVkNtRemovedFunctions[] = {
    "system", "spawnv", "spawnve", "execv", "execve", "startfile", "kill",
    "_add_dll_directory", "_remove_dll_directory", nullptr
};

static int VkNtScrubExec(PyObject* module) {
    PyObject* dict = PyModule_GetDict(module);  // Borrowed reference.
    for (const char** name = kVkNtRemovedFunctions; *name; ++name) {
        if (PyDict_DelItemString(dict, *name) < 0) PyErr_Clear();  // Absent is fine.
    }
    return 0;
}

static PyModuleDef vkNtDef;
static PyModuleDef_Slot vkNtSlots[16];

static PyObject* PyInit_vk_nt(void) {
    PyObject* original = PyInit_nt();
    if (!original) return nullptr;
    if (PyModule_Check(original)) {  // Single-phase fallback: scrub directly.
        VkNtScrubExec(original);
        return original;
    }
    static bool copied = false;
    if (!copied) {
        PyModuleDef* def = reinterpret_cast<PyModuleDef*>(original);
        vkNtDef = *def;
        int count = 0;
        if (def->m_slots) {
            for (; def->m_slots[count].slot; ++count) {
                if (count >= 14) { fwprintf(stderr, L"VishwakarmaExtension: nt slot table overflow\n"); return nullptr; }
                vkNtSlots[count] = def->m_slots[count];
            }
        }
        vkNtSlots[count].slot = Py_mod_exec;
        vkNtSlots[count].value = reinterpret_cast<void*>(&VkNtScrubExec);
        vkNtSlots[count + 1] = { 0, nullptr };
        vkNtDef.m_slots = vkNtSlots;
        copied = true;
    }
    return reinterpret_cast<PyObject*>(&vkNtDef);
}

