int wmain(int argc, wchar_t** argv) {
    if (argc < 2) {
        fwprintf(stderr, L"usage: VishwakarmaExtension.exe <entry-script.py> [args...]\n");
        return 2;
    }

    // Both tables must be in place before the interpreter initializes.
    PyImport_Inittab = vkBuiltinModules;
    PyImport_FrozenModules = VkExtraFrozenModules;

    PyStatus status;
    PyPreConfig preConfig;
    PyPreConfig_InitIsolatedConfig(&preConfig);
    preConfig.utf8_mode = 1;
    status = Py_PreInitialize(&preConfig);
    if (PyStatus_Exception(status)) Py_ExitStatusException(status);

    // The entry script's directory is the only on-disk import location: an extension
    // package is exactly one folder (main.py plus sibling modules). The stdlib comes
    // from the frozen tables only.
    wchar_t scriptPath[4096];
    if (_wfullpath(scriptPath, argv[1], 4096) == nullptr) {
        fwprintf(stderr, L"VishwakarmaExtension: cannot resolve path '%s'\n", argv[1]);
        return 2;
    }
    wchar_t scriptDir[4096];
    wcscpy_s(scriptDir, scriptPath);
    wchar_t* lastSlash = wcsrchr(scriptDir, L'\\');
    if (!lastSlash) lastSlash = wcsrchr(scriptDir, L'/');
    if (lastSlash) *lastSlash = L'\0';

    PyConfig config;
    PyConfig_InitIsolatedConfig(&config);
    config.buffered_stdio = 0;  // stdin/stdout are the IPC pipes; never buffer them.
    config.write_bytecode = 0;  // No __pycache__ in the installation directory.
    config.parse_argv = 0;
    config.site_import = 0;     // No site-packages exist; everything usable is frozen in.

    status = PyConfig_SetString(&config, &config.program_name, L"VishwakarmaExtension");
    if (!PyStatus_Exception(status))
        status = PyConfig_SetString(&config, &config.run_filename, scriptPath);
    if (!PyStatus_Exception(status))
        status = PyConfig_SetArgv(&config, argc - 1, argv + 1);
    if (!PyStatus_Exception(status)) {
        config.module_search_paths_set = 1;
        status = PyWideStringList_Append(&config.module_search_paths, scriptDir);
    }
    if (!PyStatus_Exception(status))
        status = Py_InitializeFromConfig(&config);
    PyConfig_Clear(&config);
    if (PyStatus_Exception(status)) Py_ExitStatusException(status);

    return Py_RunMain();  // Runs the entry script, finalizes, returns the exit code.
}
